#include "libftprintf.h"

int	pf_isflag(char c)
{
	return (c == '-'
			|| c == '0'
			|| c == '*'
			|| c == '.'
			|| c == ' '
			|| c == '+'
			|| c == '#'
			|| c == '\'');
}
